#include "libft.h"

void		*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	void	*cpy;

	i = 0;
	cpy = b;
	while (i < len)
	{
		*(char*)b = c;
		b++;
		i++;
	}
	return (cpy);
}
