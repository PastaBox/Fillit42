char	*ft_strcpy(char *dst, const char *src)
{
	char	*cpy;

	cpy = dst;
	while (*src != 0)
	{
		*dst = (char)*src;
		dst++;
		src++;
	}
	*dst = 0;
	return (cpy);
}
